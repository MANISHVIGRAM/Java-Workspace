package Switches;

public class Main {

	public static void main(String[] args) {
		String day = "Sunday";
		switch(day) {
		case "Sunday" : System.out.println("It is SUNDAY");
		break;
		case "Monday" : System.out.println("It is MONDAY");
		break;
		case "Tuesday" : System.out.println("It is TUESDAY");
		break;
		}
		

	}

}
